    private void addTermEqnOneBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addTermEqnOneBtnMouseClicked
        
        if (this.eqnOneNumTxtIn.getText().length() == 0) {
            //if the use has not inputted anything as the numerator, alert this issue through a dialog
            JOptionPane.showMessageDialog(null, "Please enter an integer for the numerator.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (this.eqnOneDenomTxtIn.getText().length() == 0) {
            //if the use has not inputted anything as the denominator, alert this issue through a dialog
            JOptionPane.showMessageDialog(null, "Please enter a denominator. If you do not want a fraction, you can simply set the denominator as 1.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (Long.parseLong(this.eqnOneDenomTxtIn.getText())==0) {
            //If the user inputted 0 as the denominator, send an error to the user in the form of a dialog
            JOptionPane.showMessageDialog(null, "The denominator of a number cannot be 0.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            //otherwise, the user input is valid, and we need to add this term to our ArrayList
            //parse the input to longs, and store it in appropriate variales
            long longNum = Long.parseLong(this.eqnOneNumTxtIn.getText()), longDenom = Long.parseLong(this.eqnOneDenomTxtIn.getText());
            //check to see if the user selected the radio button, if it is selected then this term will be degree one
            //store the radio choice in the following boolean variable
            boolean boolDegOne = this.eqnOneDegOne.isSelected();
            if (boolDegOne) {
                //if this term is indeed degree one, then we set the global boolean below as true, so that we know that equation one is a linear equation (and not just constant)
                boolEqnOneHasDegOneTerm = true;
            }
            //using the addTerm method in the equationSolver instance, we will add the term based on the info the user inputted
            //for the first paramter I specified a value of true, meaning that this method will add the term to equation 1
            equationSolver.addTerm(true, longNum, longDenom, boolDegOne);
            //finally, output the equation so far to the eqnOneLbl, using the printGlobalEqn() method which returns a string
            //since I specified a paramter of true, it will print out equation one and not equation two
            this.eqnOneLbl.setText(equationSolver.printGlobalEqn(true));
        }
    }//GEN-LAST:event_addTermEqnOneBtnMouseClicked

