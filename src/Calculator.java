    private void solveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_solveBtnMouseClicked
        // TODO add your handling code here:
        if (boolEqnOneHasDegOneTerm && boolEqnTwoHasDegOneTerm) {
            //if both equations have at least one degree one term, they are valid, and so we will solve the system using the solve() method, and print out the steps in a dialog
            //the solve () method accepts two parameters, arraylists for equation 1 and 2
            //those arraylists are also stored in the equationSolver instance, and are called globalEqnOne/globalEqnTwo respectively
            JOptionPane.showMessageDialog(null, ("The following is an example problem/solution generated randomly: \n" + equationSolver.solve (equationSolver.globalEqnOne, equationSolver.globalEqnTwo)), "Solution With Steps", JOptionPane.INFORMATION_MESSAGE);
            //now that we have solved the inputted equation, reset the entire frame
            reset();
            //furthermore, reset the global equation variables in the equationSolver instance, using the equationSolver's local method reset()
            equationSolver.reset();
        } else {
            //otherwise at least one of the two equations are not linear equations, hence alert user using a dialog
            JOptionPane.showMessageDialog(null, "Please ensure both of the equations has at least one degree one term.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_solveBtnMouseClicked

    private void resetBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_resetBtnMouseClicked
        //if the reset button is clicked we need to reset the JFrame using the reset method()
        reset();
        //furthermore we need to reset the arrayLists in the equationSolver instance, by using the class' reset() method
        equationSolver.reset();
    }//GEN-LAST:event_resetBtnMouseClicked

